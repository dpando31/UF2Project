package com.company.model;

public class Developer {
        //CAMPOS
        public String nombreDeveloper;
        public String localizacion;
        public String mail;
        public String sitiosWeb;
        public String fundadores;
        public String productos;
        public int numeroEmpleados;
        public int fechaFundacion;

        //METODOS
        public void setNombreDeveloper(String nombreDeveloper) {
                this.nombreDeveloper = nombreDeveloper;
        }

        public void setLocalizacion(String localizacion){
                this.localizacion = localizacion;
        }

        public void setMail(String mail){
                this.mail = mail;
        }

        public void setSitiosWeb (String sitiosWeb) {
                this.sitiosWeb = sitiosWeb;
        }

        public void setFundadores (String fundadores){
                this.fundadores = fundadores;
        }

        public void setProductos (String productos){
                this.productos = productos;
        }

        public void setNumeroEmpleados(int numeroEmpleados){
                this.numeroEmpleados = numeroEmpleados;
        }

        public void setFechaFundacion(int fechaFundacion) {
                this.fechaFundacion = fechaFundacion;
        }
}





